/* USER CODE BEGIN 4 */
void get_pitch(){
   HAL_UART_Receive_DMA(&huart1, &IMUarray[0], 31);
   for(int i = 1; i < 31; i++){
      if(IMUarray[i] == ','){
         if(cnt0 == 0){
            cnt1 = i + 1;
         }
         else if (cnt0 == 1){
            cnt2 = i + 1;
         }
         cnt0++;
      }
      else{
         switch (cnt0){
         case 0:{
            alpha[i - 1] = IMUarray[i];
            break;
         }
         case 1:{
            beta[i - cnt1] = IMUarray[i];
            break;
         }
         case 2:{
            gamm[i - cnt2] = IMUarray[i];
            break;
         }
         }
      }
      if(IMUarray[i] == '\r'){
         cnt3 = i + 1;
         break;
      }
   }
   for (int j = cnt1 - 2; j < 7; j++){
      alpha[j] = '0';
   }
   for (int k = cnt2 - (cnt1); k < 8; k++){
      beta[k - 1] = '0';
   }
   for (int l = cnt3 - (cnt2); l < 8; l++){
      gamm[l - 1] = '0';
   }
   cnt0 = 0;

   roll_tp = strtod(alpha, &pos);
   pitch_tp = strtod(beta, &pos);
   yaw_tp = strtod(gamm, &pos);

   cnt4 += 1;
   pitch = roll_tp;
   roll = pitch_tp;
   yaw = yaw_tp;
}
